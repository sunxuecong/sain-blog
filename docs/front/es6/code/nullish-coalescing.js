/* ?? 运算符 */
const a = 0 ?? 'sain'         // 0
const b = '' ?? 'sain'        // ''
const c = null ?? 'sain'      // 'sain'
const d = undefined ?? 'sain' // 'sain'

/* || 运算符 */
const A = 0 || 'sain'         // 'sain'
const B = '' || 'sain'        // 'sain'
const C = null || 'sain'      // 'sain'
const D = undefined || 'sain' // 'sain'
